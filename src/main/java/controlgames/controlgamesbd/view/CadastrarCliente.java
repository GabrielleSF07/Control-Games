    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        String nome = txtNome.getText();
        String email = txtEmail.getText();
        String senha = txtSenha.getText();
        String idadeStr = txtIdade.getText();

        if (nome.isEmpty() || email.isEmpty() || senha.isEmpty() || idadeStr.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Preencha todos os campos corretamente");
            return;
        } else {
             int idade;
        try {
            idade = Integer.parseInt(idadeStr);
            
            UsuariosDAO dao = new UsuariosDAO();
            Usuarios u = new Usuarios();
            
            u.setEmail(email);
            u.setIdade(idade);
            u.setNome(nome);
            u.setSenha(senha);
            
            dao.insereUsuario(u);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao cadastrar us√∫ario: " + e.getMessage());
            return;
        }
           
        }

       

    }//GEN-LAST:event_btnCadastrarActionPerformed

